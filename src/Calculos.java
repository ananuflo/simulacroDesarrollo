    public void configurarValidacion(){
        
        ValidationGroup grupo = panelValidador.getValidationGroup();
        
        grupo.add(jTextFieldNum1, StringValidators.REQUIRE_VALID_INTEGER, StringValidators.REQUIRE_NON_NEGATIVE_NUMBER);
        
        panelValidador.addChangeListener(e->{ 
            boolean sinErrores = panelValidador.getProblem() == null;
            btnCalcular.setEnabled(sinErrores);
            });
        
        boolean sinErrorInicial = panelValidador.getProblem() == null;
        btnCalcular.setEnabled(sinErrorInicial);
        
    }
    
    public void cambiarImagen(){
        
        String op = jComboOperacion.getSelectedItem().toString();
        String rutaImg = "";
        
        switch(op){
            case "Suma":
                rutaImg = "/resources/sumaa.png";
                    break;
            case "Resta":
                rutaImg = "/resources/restar.png";
                break;
            case "Multiplicación":
                rutaImg = "/resources/multiplicacionn.png";
                break;
            case "División":
                rutaImg = "/resources/division.png";
                break;
                                  
        }
        
        if(!rutaImg.isEmpty()){
            ImageIcon img = new ImageIcon(getClass().getResource(rutaImg));
            jLabelImagen.setIcon(img);
        }
    }

