    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        
        String operacion = jComboOperacion.getSelectedItem().toString();
        String num1 = jTextFieldNum1.getText();
        int num1Entero = Integer.parseInt(num1);
        String num2 = spnNum2.getValue().toString();
        int num2Entero = Integer.parseInt(num2);
        
        
        int resultado = 0;
        
        switch(operacion){
            
            case "Suma":
                resultado = num1Entero + num2Entero;
                break;
            case "Resta":
                resultado = num1Entero - num2Entero;
                break;
            case "Multiplicación":
                resultado = num1Entero * num2Entero;
                break;
            case "División":
                resultado = num1Entero / num2Entero;
                break;
                        
        }
        
        jLabelResultado.setText("Resultado = " + resultado);
        
        int opcion = JOptionPane.showConfirmDialog(this, "¿Desea añadir esa operación a la tabla?","Pregunta", JOptionPane.YES_NO_OPTION);
        
        if(opcion == JOptionPane.YES_OPTION){
          crearCalculo = new Calculo(operacion, num1Entero, num2Entero,resultado);
        dispose();  
        }else{
            JOptionPane.showMessageDialog(this, "¡OK! Sigamos calculando...", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            crearCalculo = null;
            return;
        }
        
    }//GEN-LAST:event_btnCalcularActionPerformed

